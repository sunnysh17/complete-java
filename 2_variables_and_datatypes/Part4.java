class Part4{
    public static void main(String[] args) {
        int x = -5; //converting into binary string will be in 2's complement format
        int y = 5;
        System.out.println(Integer.toBinaryString(x)); //present inside java.lang.Integer 
        System.out.println(Integer.toBinaryString(y)); //present inside java.lang.Integer
        //Only available for Integer Data type 

    }
}